void main() {
  // static variables

  int number1 = 10;
  print(number1);

  String name = "Prince Singh";
  print(name);

  double decimal = 10.00;
  print(decimal);

  bool isTrue = true;
  print(isTrue);

  // dynamic can have any type and not recommended to use
  dynamic lastName = "Singh";
  lastName = 10;

  print(lastName);
}

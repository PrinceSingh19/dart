void main() {
  // Reassigning the value to the string
  String name = "Prince";
  print(name);

  name = "Prinshu";
  print(name);

  // Reassigning the values to the int
  int number = 10;
  print(number);

  number = 20;
  print(number);

  // Reassigning the booleans
  bool isTrue = true;
  print(isTrue);

  isTrue = false;
  print(isTrue);
}
